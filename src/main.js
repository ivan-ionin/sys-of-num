class SystemOfNumeration {

    constructor(set) {
        if (!this.setSymbols(set)) {
            console.log('Bad symbols set');
        }
    }

    getSymbols() {
        return this._symbols;
    }

    setSymbols(set) {
        this._symbols = false;
        this._system = false;
        if (undefined === set) {
            this._symbols = symbolsSets[defaultSymbolsSet];
        } else {
            if (Array.isArray(set)) {
                this._symbols = set;
            } else if ('string' === typeof set) {
                if (undefined !== symbolsSets[set]) {
                    this._symbols = symbolsSets[set];
                } else {
                    this._symbols = set.split('');
                }
            }
        }
        if (this._symbols) {
            this._system = this._symbols.length;
        }
        return !!(this._system);
    }

    getSystem() {
        return this._system;
    }

    getSymbolsSetByName(set) {
        if (undefined === symbolsSets[set]) {
            return [];
        } else {
            return symbolsSets[set];
        }
    }

    encode(num) {
        let res = '';
        if (undefined === num) {
            num = Date.now();
        }
        if (num < this._system) {
            res = this._symbols[num];
        } else {
            let i = parseInt(num / this._system, 10);
            let s = num - (i * this._system);
            res = this.encode(s);
            res = this.encode(i) + res;
        }
        return res;
    };

    decode(str) {
        let res = 0, i;
        for (i = str.length - 1; i >= 0; i --) {
            let pow = str.length - i - 1;
            let s = this._symbols.indexOf(str[i]);
            res += s * Math.pow(this._system, pow);
        }
        return res;
    };
}

module.exports.create = (set) => {
    return new SystemOfNumeration(set);
};